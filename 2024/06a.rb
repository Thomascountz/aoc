class Guard
  TURN = {
    up: :right,
    right: :down,
    down: :left,
    left: :up
  }

  OBSTACLE = "#"

  attr_accessor :direction
  attr_reader :location, :previous_locations

  def initialize(map, row_size)
    @map = map
    @row_size = row_size
    @location = nil
    @direction = nil
    @previous_locations = []
  end

  def location=(loc)
    @location = loc
    @previous_locations << loc
  end

  def step
    next_location = send(direction)
    if next_location.negative? ||
        next_location >= map.size ||
        direction == :left && next_location % row_size == row_size - 1 ||
        direction == :right && next_location % row_size == 0
      nil
    elsif map.at(next_location) == OBSTACLE
      self.direction = TURN[direction]
    else
      self.location = next_location
    end
  end

  def to_s
    {location:, direction:, previous_locations:}.to_s
  end

  private

  attr_reader :map, :row_size
  attr_writer :previous_locations

  def up = location - row_size

  def right = location + 1

  def down = location + row_size

  def left = location - 1
end

