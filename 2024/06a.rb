DIRECTION = {
  "^" => :up,
  ">" => :right,
  "V" => :down,
  "<" => :left
}

guard = Guard.new(map, row_size)
DIRECTION.keys.each do |cell|
  if (loc = map.index(cell))
    guard.location = loc
    guard.direction = DIRECTION[cell]
  end
end

